    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        boolean find = false;
        if (txtSearch.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please input id!", "Warning", JOptionPane.ERROR_MESSAGE);
            txtSearch.setText("");
            return;
        } else if (!txtSearch.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Please input correct Id!", "Warning", JOptionPane.ERROR_MESSAGE);
            txtSearch.setText("");
            return;
        }

        for (Person p : sys.getPersonDirectory().getPersons()) {
            if (p.getId() == Integer.parseInt(txtSearch.getText())) {
                find = true;
            }
        }
        if (!find) {
            tblView.clearSelection();
            JOptionPane.showMessageDialog(this, "No person is Found!", "Warning", JOptionPane.ERROR_MESSAGE);
            txtSearch.setText("");
            return;
        }
        person = sys.getPersonDirectory().RetrievePerson(Integer.parseInt(txtSearch.getText()));
        populateEncounters();
        txtName.setText(person.getName());

    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (txtHigh.getText().isEmpty() || txtLow.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please input Vital Sign!", "Warning", JOptionPane.ERROR_MESSAGE);
            txtHigh.setText("");
            txtLow.setText("");
            return;
        } else if ((!txtHigh.getText().matches("\\d+")) || (!txtLow.getText().matches("\\d+"))) {
            JOptionPane.showMessageDialog(this, "Please input correct Vital Sign!", "Warning", JOptionPane.ERROR_MESSAGE);
            txtHigh.setText("");
            txtLow.setText("");
            return;
        } else if (Integer.parseInt(txtHigh.getText()) <= Integer.parseInt(txtLow.getText())) {
            JOptionPane.showMessageDialog(this, "High Pressure cannot be lower than Low Pressure!", "Warning", JOptionPane.ERROR_MESSAGE);
            txtHigh.setText("");
            txtLow.setText("");
            return;
        }
        JOptionPane.showMessageDialog(this, "Vital Signs Saved!");

        VitalSigns vitalSign = new VitalSigns(Integer.parseInt(txtHigh.getText()), Integer.parseInt(txtLow.getText()));
        System.out.println(vitalSign);
        Encounter encounter = new Encounter(vitalSign);
        System.out.println(encounter);

        EncounterHistory encounterHistory = person.getEncounterHistory();
        ArrayList<Encounter> history = encounterHistory.getHistory();
        history.add(new Encounter(new VitalSigns(Integer.parseInt(txtHigh.getText()), Integer.parseInt(txtLow.getText()))));
        System.out.println(history);
        populateEncounters();
        
        txtHigh.setText("");
        txtLow.setText("");
    }//GEN-LAST:event_btnSaveActionPerformed


