    private void populateEncounters() {
        DefaultTableModel model = (DefaultTableModel) tblView.getModel();
        model.setRowCount(0);
        System.out.println(person.getEncounterHistory().getHistory().size());
        for (int i = 0; i < person.getEncounterHistory().getHistory().size(); i++) {
            Object[] row = new Object[2];
            row[0] = i + 1;
            row[1] = person.getEncounterHistory().getHistory().get(i);
            model.addRow(row);

        }
    }

